public class Task5 {

    /*
    5. Bir mesajı do-while döngüsü kullanarak ekrana 5 kez yazdıran bir Java programı yazın.
     */

    public static void main(String[] args) {

        // Başlangıç değeri
        int count = 0;

        // do-while döngüsü kullanarak mesajı 5 kez yazdırma
        do {
            System.out.println("Merhaba, ben Merve!");
            count++;
        } while (count < 5);

    }
}
