public class Task6 {

    /*
    6. 1'den 100'e kadar olan sayıları for döngüsü kullanarak ekrana yazdıran bir Java programı yazın.
     */

    public static void main(String[] args) {

        // 1'den 100'e kadar olan sayıları ekrana yazdırmak için for döngüsü kullanıyoruz
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
    }
}
