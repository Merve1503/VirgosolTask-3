public class Task7 {

    /*
    7. 1'den 100'e kadar olan sayıları while döngüsü kullanarak ekrana yazdıran bir Java programı yazın.
     */

    public static void main(String[] args) {

        // Başlangıç değeri
        int sayi = 1;

        // while döngüsü kullanarak 1'den 100'e kadar olan sayıları ekrana yazdırma
        while (sayi <= 100) {
            System.out.println(sayi);
            sayi++;
        }
    }
}
